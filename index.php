                    // Initialize Swiper
                    new Swiper(".categorySlider", {
                        slidesPerView: 4,
                        spaceBetween: 60,
                        centeredSlides: true,
                        loop: true,
                        grabCursor: true,
                        // autoplay: {
                        //     delay: 3000,  // Adjust autoplay speed (milliseconds)
                        //     disableOnInteraction: false,  // Keep autoplay active after user interaction
